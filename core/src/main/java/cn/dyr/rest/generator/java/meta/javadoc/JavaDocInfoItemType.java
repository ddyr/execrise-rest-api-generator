package cn.dyr.rest.generator.java.meta.javadoc;

/**
 * 表示 javadoc 内容的类型
 *
 * @author DENG YURONG
 * @version 0.1.0001
 */
public enum JavaDocInfoItemType {

    /**
     * author 的 javadoc 类型
     */
    AUTHOR,
    /**
     * version 的 javadoc 类型
     */
    VERSION,
    /**
     * param 的 javadoc 类型
     */
    PARAM,
    /**
     * return 的 javadoc 类型
     */
    RETURN,
    /**
     * throws 的 javadoc 类型
     */
    THROWS

}
